// %d is used to integer value
//%f  is used to float value
//%c is used to charcter value
#include<stdio.h>
void main()
{
	printf("integer value = %d hello!\n",12);
	printf("float value = %f\n",7.8);   // 7.800000
	printf("float value = %.3f\n",7.8);   //7.800
	printf("character value = %c ",'@');
	
}
